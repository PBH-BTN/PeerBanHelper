<template>
  <div>
    <banLogFilters v-model="filters" />
    
    <a-table
      :stripe="true"
      :columns="columns"
      :data="list"
      :loading="tableLoading"
      :pagination="{
        total,
        current,
        pageSize,
        showPageSize: true,
        baseSize: 4,
        bufferSize: 1
      }"
      column-resizable
      size="medium"
      class="banlog-table"
      @sorter-change="sorterChange"
      @page-change="changeCurrent"
      @page-size-change="changePageSize"
    >
    <template #banAt="{ record }">
      <a-space fill direction="vertical">
        <a-typography-text><icon-stop /> {{ d(record.banAt, 'long') }}</a-typography-text>
        <a-typography-text
          ><icon-clock-circle />
          {{
            record.unbanAt ? d(record.unbanAt, 'long') : t('page.banlog.banlogTable.notUnbanned')
          }}</a-typography-text
        >
      </a-space>
    </template>
    <template #peerAddress="{ record }">
      <a-typography-text copyable code>
        <queryIpLink :ip="record.peerIp" style="color: var(--color-text-2)">
          {{ formatIPAddressPort(record.peerIp, record.peerPort) }}
        </queryIpLink>
      </a-typography-text>
    </template>
    <template #peerStatus="{ record }">
      <a-space fill style="justify-content: space-between">
        <a-space fill direction="vertical">
          <a-typography-text
            ><icon-arrow-up class="green" />
            {{ formatFileSize(record.peerUploaded) }}</a-typography-text
          >
          <a-typography-text
            ><icon-arrow-down class="red" />
            {{ formatFileSize(record.peerDownloaded) }}</a-typography-text
          >
        </a-space>
        <a-tooltip :content="(record.peerProgress * 100).toFixed(2) + '%'">
          <a-progress :percent="record.peerProgress" size="mini" />
        </a-tooltip>
      </a-space>
    </template>
    <template #peerId="{ record }">
      <p>
        {{ record.peerId ? record.peerId : t('page.banlist.banlist.listItem.empty') }}
        <a-tooltip
          :content="
            record.peerClientName ? record.peerClientName : t('page.banlist.banlist.listItem.empty')
          "
        >
          <icon-info-circle />
        </a-tooltip>
      </p>
    </template>
    <template #torrentName="{ record }">
      <a-tooltip
        :content="
          t('page.banlog.banlogTable.column.torrentName.tooltip', {
            downloader: record.downloader?.name,
            hash: record.torrentInfoHash
          })
        "
      >
        <p>{{ record.torrentName }}</p>
      </a-tooltip>
    </template>
    <template #torrentSize="{ record }">
      <a-tooltip :content="`Hash: ${record.torrentInfoHash}`">
        <p>{{ formatFileSize(record.torrentSize) }}</p>
      </a-tooltip>
    </template>
    </a-table>
  </div>
</template>
<script setup lang="ts">
import queryIpLink from '@/components/queryIpLink.vue'
import banLogFilters from './banLogFilters.vue'
import { getBanlogs, type BanLogFilters } from '@/service/banLogs'
import { useAutoUpdatePlugin } from '@/stores/autoUpdate'
import { useEndpointStore } from '@/stores/endpoint'
import { formatFileSize } from '@/utils/file'
import { formatIPAddressPort } from '@/utils/string'
import { computed, ref, watch } from 'vue'
import { useI18n } from 'vue-i18n'
import { usePagination } from 'vue-request'

const forceLoading = ref(true)
const endpointState = useEndpointStore()
const { t, d } = useI18n()
const filters = ref<BanLogFilters>({})

// Custom wrapper function that includes filters
const fetchBanLogs = (params: { page: number; pageSize?: number; sorter?: string }) => {
  return getBanlogs({ ...params, filters: filters.value })
}

const { data, total, current, loading, pageSize, changeCurrent, changePageSize, refresh, run } =
  usePagination(
    fetchBanLogs,
    {
      defaultParams: [
        {
          page: 1,
          pageSize: 10
        }
      ],
      pagination: {
        currentKey: 'page',
        pageSizeKey: 'pageSize',
        totalKey: 'data.total'
      },
      cacheKey: (params) =>
        `${endpointState.endpoint}-banlogs-${params?.[0].page || 1}-${params?.[0].pageSize || 10}-${JSON.stringify(filters.value)}`,
      onAfter: () => {
        forceLoading.value = false
      }
    },
    [useAutoUpdatePlugin]
  )

watch([pageSize, current], () => {
  forceLoading.value = true
})

watch(() => endpointState.endpoint, refresh)

// Watch filters and trigger refresh (prevent infinite loops with careful change detection)
watch(filters, (newFilters, oldFilters) => {
  // Only trigger if filters actually changed and are not empty objects
  const newFilterStr = JSON.stringify(newFilters || {})
  const oldFilterStr = JSON.stringify(oldFilters || {})
  
  if (newFilterStr !== oldFilterStr) {
    // Add a small delay to prevent rapid-fire updates
    setTimeout(() => {
      forceLoading.value = true
      run({
        page: 1, // Reset to first page when filters change
        pageSize: pageSize.value
      })
    }, 50)
  }
}, { deep: true })

const tableLoading = computed(() => {
  return forceLoading.value || loading.value || !list.value
})

const columns = [
  {
    title: () =>
      t('page.banlog.banlogTable.column.banTime') +
      '/' +
      t('page.banlog.banlogTable.column.unbanTime'),
    slotName: 'banAt',
    dataIndex: 'banAt',
    sortable: {
      sortDirections: ['ascend', 'descend'] as ('ascend' | 'descend')[],
      sorter: true
    },
    width: 210
  },
  {
    title: () => t('page.banlog.banlogTable.column.peerAddress'),
    slotName: 'peerAddress',
    width: 230
  },
  {
    title: () => t('page.banlog.banlogTable.column.peerId'),
    slotName: 'peerId',
    width: 120
  },
  {
    title: () => t('page.banlog.banlogTable.column.trafficSnapshot'),
    slotName: 'peerStatus',
    dataIndex: 'peerUploaded',
    sortable: {
      sortDirections: ['ascend', 'descend'] as ('ascend' | 'descend')[],
      sorter: true
    },
    width: 150
  },
  {
    title: () => t('page.banlog.banlogTable.column.torrentName'),
    dataIndex: 'torrentName',
    slotName: 'torrentName',
    ellipsis: true,
    tooltip: true
  },
  {
    title: () => t('page.banlog.banlogTable.column.torrentSize'),
    slotName: 'torrentSize',
    dataIndex: 'torrentSize',
    sortable: {
      sortDirections: ['ascend', 'descend'] as ('ascend' | 'descend')[],
      sorter: true
    },
    width: 120
  },
  {
    title: () => t('page.banlog.banlogTable.column.description'),
    dataIndex: 'description',
    ellipsis: true,
    tooltip: true
  }
]
const list = computed(() => data.value?.data?.results || [])
const sorterChange = (dataIndex: string, direction: string) => {
  if (!direction)
    run({
      page: current.value,
      pageSize: pageSize.value
    })
  else
    run({
      page: current.value,
      pageSize: pageSize.value,
      sorter: `${dataIndex}|${direction}`
    })
}
</script>

<style scoped>
.red {
  color: rgb(var(--red-5));
}
.green {
  color: rgb(var(--green-5));
}
</style>
